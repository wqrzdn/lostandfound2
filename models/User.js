const mongoose = require('mongoose');
const bcrypt = require('bcryptjs');

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true
  },
  password: {
    type: String,
    required: function() {
      // Password is required only if googleId is not provided
      return !this.googleId;
    }
  },
  googleId: {
    type: String,
    unique: true,
    sparse: true
  },
  
  role: {
    type: String,
    enum: ['user', 'admin'],
    default: 'user'
  },
  profileImage: {
    type: String
  },
  phone: {
    type: String,
    trim: true
  },
  items: [{
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Item'
  }],
  isActive: {
    type: Boolean,
    default: true
  },
  lastLogin: {
    type: Date
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

// Password hashing middleware
UserSchema.pre('save', async function(next) {
  // Update the updatedAt field
  this.updatedAt = Date.now();
  
  // Only hash the password if it exists, has been modified, and is not empty
  if (!this.password || !this.isModified('password')) return next();
  
  try {
    const salt = await bcrypt.genSalt(10);
    this.password = await bcrypt.hash(this.password, salt);
    next();
  } catch (err) {
    next(err);
  }
});

// Method to check password
UserSchema.methods.matchPassword = async function(enteredPassword) {
  // If user has no password (Google auth user), always return false
  if (!this.password) return false;
  return await bcrypt.compare(enteredPassword, this.password);
};

// Method to update last login
UserSchema.methods.updateLoginTimestamp = async function() {
  this.lastLogin = Date.now();
  return this.save();
};

// Virtual for user's full profile
UserSchema.virtual('profile').get(function() {
  return {
    id: this._id,
    name: this.name,
    email: this.email,
    role: this.role,
    profileImage: this.profileImage,
    phone: this.phone,
    itemCount: this.items.length,
    isActive: this.isActive,
    createdAt: this.createdAt
  };
});

module.exports = mongoose.model('User', UserSchema);